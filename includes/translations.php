function t(string $key, string $lang = ''): string
{
    if (empty($lang)) {
        $lang = getCurrentLanguage();
    }

    $translations = getTranslations();
    return $translations[$lang][$key] ?? $translations['en'][$key] ?? $key;
}

function getCurrentLanguage(): string
{
    $lang = $_GET['lang'] ?? $_SESSION['lang'] ?? getSetting('default_language', 'en');
    $available = explode(',', getSetting('available_languages', 'en,sl'));

    if (!in_array($lang, $available, true)) {
        $lang = 'en';
    }

    $_SESSION['lang'] = $lang;
    return $lang;
}

function getAvailableLanguages(): array
{
    $langs = explode(',', getSetting('available_languages', 'en,sl'));
    $result = [];
    foreach ($langs as $l) {
        $l = trim($l);
        if ($l) {
            $result[$l] = t('lang_' . $l, 'en');
        }
    }
    return $result;
}

function getLocalizedSetting(string $key, string $default = ''): string
{
    $lang = getCurrentLanguage();
    $value = getSetting($key . '_' . $lang, '');
    if (empty($value)) {
        $value = getSetting($key . '_en', $default);
    }
    return $value;
}
